# Q9. 변수 바인딩이란 무엇인가요? 파이썬에서 `x = y`는 어떤 의미인가요?

# 이름을 값에 연결하는 것. x = y는 y가 참조하는 객체를 x도 참조하게 만든다.


# ✅ 정답 요약
# 변수 바인딩이란 이름(변수)을 객체에 연결하는 과정을 의미합니다.
# x = y는 y가 참조하고 있는 객체를 x도 함께 참조하게 만든다는 뜻입니다.
# 즉, 값을 복사하는 것이 아니라 참조를 공유합니다.


# 🧠 배경 개념: 파이썬에서의 변수는 "이름표(name tag)"일 뿐
# 파이썬은 변수가 값을 저장하는 것처럼 보이지만, 실제로는 **객체에 대한 참조(reference)**를 저장합니다.

# 즉, 변수는 **객체에 붙은 이름표(label)**에 가깝습니다.


# 🔍 예제: 기본 바인딩
a = [1, 2, 3]
b = a


# 메모리 구조 (시각적으로 표현)
# [1, 2, 3]  ← list 객체

# a ─────┐
#        ▼
# b ─────┘  (같은 객체를 가리킴)


# 결과
print(a is b)  # True (같은 객체)
print(id(a))   # 예: 140330123456
print(id(b))   # 같은 값


# 🧪 내부 동작 설명
# a = [1, 2, 3]: 리스트 객체가 생성되고 변수 a가 그 객체에 바인딩됨
# b = a: 변수 b도 같은 객체를 가리킴
# 따라서 b.append(4)를 하면 → a도 함께 변경된 것처럼 보임


# ⚠️ 복사와 바인딩은 다르다
x = [1, 2, 3]
y = x           # 단순 바인딩
z = x[:]        # 얕은 복사

print(x is y)   # True
print(x is z)   # False


# 📚 바인딩의 특징
# 특징	                        설명
# 객체 중심 언어	            변수는 이름표일 뿐, 본질은 객체에 있음
# 값 복사 아님	                x = y는 값을 복사하지 않음
# mutable vs immutable	    가변 객체는 바인딩 후 수정 가능


# 🔁 불변 객체에서도 같은 원리
x = 10
y = x
x += 1

print(x)  # 11
print(y)  # 10
# → x += 1은 기존 객체 수정이 아니라 새로운 객체를 바인딩한 것


